package model;

/**
 *
 * @author PLATINADO
 */
public class Pacote {
    
    private int peso;
    private int valor;
    private int id;

    public Pacote(int peso, int valor, int id) {
        this.peso = peso;
        this.valor = valor;
        this.id = id;
    }

    public int getPeso() {
        return peso;
    }

    public int getValor() {
        return valor;
    }

    public int getId() {
        return id;
    }
    
    
    
    
    
    
}
